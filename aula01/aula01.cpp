#include <iostream>
/*
    C++ contém C, mas C não contém C++;
*/
int main() {

    std::cout << "Olá Mundo!" << std::endl; // Essa "função" insere coisas no console e depois termina a linha; 
    std::cin.get(); // Essa função vai esperar um retorno (tecla Enter);
    return 0; // 0 é a saída padrão da main();
    
}